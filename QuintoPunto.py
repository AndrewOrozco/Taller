# Programa que me calcula numero mayor de N Entradas
a = float(input(" Ingrese el nuemro {} ".format(1)))
b = float(input(" Ingrese el nuemro {} ".format(2)))
c = float(input(" Ingrese el nuemro {} ".format(3)))

if b<a>c:
  print("El nuemro mayor: ", a)
elif a<b>c:
  print("El nuemro mayor: ", b)
elif a<c>b:
  print("El nuemro mayor: ", c)